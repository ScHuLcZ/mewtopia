        <h2>Help us rescue cats</h2>
            <p>
                Mewtopia Cat Rescue is a non-profit community service organization that rescues homeless, abused, abandoned or owner-relinquished cats. 
                We help reduce the unwanted cat population by
            </p>
            <ul>
                <li><strong>placing</strong> cats with responsible owners through adoption</li><br><br>
                <li><strong>helping</strong> trap, alter and release stray/feral cats</li><br><br>
                <li><strong>educating</strong> the public on responsible animal ownership</li><br>
            </ul>
